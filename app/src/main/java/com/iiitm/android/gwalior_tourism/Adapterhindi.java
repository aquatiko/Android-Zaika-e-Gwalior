package com.iiitm.android.gwalior_tourism;

public class Adapterhindi {



    public  String name;
    public int image;
    public Adapterhindi(String name, int image)
    {
        this.name= name;
        this.image= image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
